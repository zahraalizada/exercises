<?php
/*

### Boolean to String Conversion  #

Boolean to String Conversion


boolToString(true) ➞ "true"
boolToString(false) ➞ "false"


*/

// ================= Solution =====================
function boolToString($flag) {
    if($flag == 1){
        echo "true";
    } else{
        echo "false";
    }

}


boolToString(false);

?>